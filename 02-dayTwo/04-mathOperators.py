# There are 5 math operations that can be run in python i.e :
# Addition '+'
# Subtraction '-'
# Multiplication '*'
# Division '/'
# Exponential '**'

# The order of Operations can be summarised by the Acronym PEMDAS 
# P - Parenthesis
# E - Exponential
# M - Multiplication
# D - Division 
# A - Addition
# S - Subtraction

# Example 

print (3 * 3 + 3 / 3 - 1)
print(3 * (3 + 3 )/ 3 -1 )
print(3 * 3 + 3 / (3 - 1) )