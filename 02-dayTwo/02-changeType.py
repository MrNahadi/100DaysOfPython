# You can change the type of data of a variable by simply using datatype(variable)
# Example  if you want to change a sting to an integer it will be int('StringVariable')
# To check the type of data you can use the function type()

a = 123
print(a)
print(type(a))

b = str(a)
print(b)
print(type(b))

c = float(b)
print(c)
print(type(c))